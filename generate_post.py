def apply_template(lang, title, date, desc, sommaire, content, image_url):
    if lang == 'fr':
        tpl_path = "/opt/data/website-zamania/blog/template.html"
        out_path = f"/opt/data/website-zamania/blog/{slug}.html"
    elif lang == 'en':
        tpl_path = "/opt/data/website-zamania/en/blog/template.html"
        out_path = f"/opt/data/website-zamania/en/blog/{slug}.html"
    elif lang == 'ar':
        tpl_path = "/opt/data/website-zamania/ar/blog/template.html"
        out_path = f"/opt/data/website-zamania/ar/blog/{slug}.html"
        
    with open(tpl_path, 'r', encoding='utf-8') as f:
        html = f.read()
        
    html = html.replace("{{TITLE}}", title)
    html = html.replace("{{DATE}}", date)
    html = html.replace("{{DESCRIPTION}}", desc)
    html = html.replace("{{SOMMAIRE}}", sommaire)
    html = html.replace("{{CONTENT}}", content)
    html = html.replace("{{IMAGE_URL}}", image_url)
    
    with open(out_path, 'w', encoding='utf-8') as f:
        f.write(html)
        
