def update_index(lang, title, date, desc, slug):
    if lang == 'fr':
        idx_path = "/opt/data/website-zamania/blog/index.html"
        link = f"{slug}.html"
        img = f"../images/blog/img-{slug}.jpg"
        read_more = "Lire l'article"
    elif lang == 'en':
        idx_path = "/opt/data/website-zamania/en/blog/index.html"
        link = f"{slug}.html"
        img = f"../../images/blog/img-{slug}.jpg"
        read_more = "Read article"
    elif lang == 'ar':
        idx_path = "/opt/data/website-zamania/ar/blog/index.html"
        link = f"{slug}.html"
        img = f"../../images/blog/img-{slug}.jpg"
        read_more = "اقرأ المقال"
        
    with open(idx_path, 'r', encoding='utf-8') as f:
        html = f.read()
        
    card = f'''
            <article class="article-card">
                <img src="{img}" alt="{title}" class="article-image">
                <div class="article-content">
                    <span class="article-date">{date}</span>
                    <h2 class="article-title">{title}</h2>
                    <p class="article-excerpt">{desc}</p>
                    <a href="{link}" class="article-link">{read_more}</a>
                </div>
            </article>'''
            
    html = html.replace("<!-- ARTICLES_LIST_START -->", "<!-- ARTICLES_LIST_START -->\n" + card)
    
    with open(idx_path, 'w', encoding='utf-8') as f:
        f.write(html)

